import sys
if len(sys.argv) > 1:
    str1 = ""
    strl = ' '.join(sys.argv[1::]).upper()
    for char in strl:
        if char.isalnum() or char == ' ':
            if char.isalnum(): str1 = str1 + MORSE[char] + ' '
            elif char == ' ': str1 = str1 + '/' + ' '
        else: sys.exit("ERROR")
    print(str1)
else:
    sys.exit("Program needs arguments")